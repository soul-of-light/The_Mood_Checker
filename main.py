mood = input("What's your mood? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Come on smile a little bit.")
elif mood == "excited":
    print("You are so excited.")
elif mood == "relaxed":
    print("Let`s chill on the couch!")
else:
    print("I don't recognize this mood.")
